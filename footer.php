            function createNestedList(headings, parentElement) {
                let index = 0;
                while (index < headings.length) {
                    const heading = headings[index];
                    const headingLevel = parseInt(heading.tagName.charAt(1));
                    const liElement = document.createElement("li");
                    liElement.classList.add("s-investment-article-detail-table-of-content-item");
                    const aElement = document.createElement("a");
                    aElement.classList.add("s-investment-article-detail-table-of-content-link");
                    aElement.textContent = heading.textContent;
                    aElement.setAttribute("href", `#${heading.id}`);
                    liElement.appendChild(aElement);
                    const nestedHeadings = [];
                    index++;
                    while (index < headings.length) {
                        const nextHeading = headings[index];
                        const nextHeadingLevel = parseInt(nextHeading.tagName.charAt(1));

                        if (nextHeadingLevel > headingLevel) {
                            nestedHeadings.push(nextHeading);
                            index++;
                        } else {
                            break;
                        }
                    }
                    if (nestedHeadings.length > 0) {
                        const nestedUlElement = document.createElement("ul");
                        nestedUlElement.classList.add("s-investment-article-detail-table-of-content-list");
                        liElement.appendChild(nestedUlElement);
                        createNestedList(nestedHeadings, nestedUlElement);
                    }
                    parentElement.appendChild(liElement);
                }
            }
            createNestedList(nonEmptyHTags, ulElement);
            tocContainer.appendChild(ulElement);
        }

